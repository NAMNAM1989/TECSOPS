function cutoffToday(time: string, dateStr: string): string {
  if (!time) return "";
  const [h, m] = time.split(":").map(Number);
  const dayMatch = dateStr.match(/(\d{2})APR/i);
  const day = dayMatch ? parseInt(dayMatch[1]) : 4;
  const d = new Date(2026, 3, day, h, m, 0);
  return d.toISOString();
}

